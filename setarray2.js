// Create a set
var set = new Set([1, 2, 3, 4, 5]);

// Search for an element in the set
var found = set.has(3);

console.log(found); // true

// Create an array
var array = [1, 2, 3, 4, 5];

// Search for an element in the array
var found = array.indexOf(3) !== -1;

console.log(found); // true
